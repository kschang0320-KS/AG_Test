// ─── 4. 반복 표현 축약 ────────────────────────────────────────────────
const REDUNDANCY_MAP = [
  [/개선이\s*필요한\s*것\s*같습니다/g, '개선이 필요합니다'],
  [/개선이\s*됐으면\s*합니다/g, '개선을 건의드립니다'],
  [/더\s*나아졌으면\s*합니다/g, '개선을 건의드립니다'],
  [/\.{2,}/g, '.'],
  [/([.!?])\s*([.!?])+/g, '$1'],
];

// ─── 5. 이모티콘·특수기호 정제 ───────────────────────────────────────
function cleanSpecial(text) {
  return text
    .replace(/[ㅠㅜㅡㅎㅋ]{2,}/g, '') // 자음 이모티콘
    .replace(/!{2,}/g, '.') // 느낌표 중복
    .replace(/\?{2,}/g, '?') // 물음표 중복
    .replace(/~+/g, '') // 물결
    .replace(/[\u{1F600}-\u{1F64F}\u{1F300}-\u{1F5FF}\u{1F680}-\u{1F6FF}\u{2600}-\u{26FF}]/gu, '') // 이모지
    .replace(/\s{2,}/g, ' ')
    .trim();
}

// ─── 6. 문장 끝 정리 ─────────────────────────────────────────────────
function normalizeSentenceEnds(text) {
  // 마침표 없이 끝나는 문장에 마침표 추가
  return text
    .split(/(?<=[.!?])\s+/)
    .map(s => {
      const t = s.trim();
      if (!t) return '';
      if (/[가-힣]$/.test(t)) return t + '.';
      return t;
    })
    .filter(Boolean)
    .join(' ');
}

// ─── 7. 문장 접속어 자연스럽게 ────────────────────────────────────────
function polishConnectors(text) {
  return text
    .replace(/그리고\s*/g, '또한 ')
    .replace(/그래서\s*/g, '이에 ')
    .replace(/근데\s*/g, '그러나 ')
    .replace(/그런데\s*/g, '그러나 ')
    .replace(/하지만\s*(?=\S)/g, '다만, ')
    .replace(/근데요\s*/g, '그러나 ')
    .replace(/또\s*/g, '아울러 ')
    .replace(/그냥\s*/g, '');
}

// ─── 메인 함수 ────────────────────────────────────────────────────────
export function anonymizeText(text) {
  if (!text || text.trim().length === 0) return text;

  let result = text;

  // 순서: 특수문자 → 1인칭 → 강도부사 → 구어체 어미 → 접속어 → 반복 → 문장 끝
  result = cleanSpecial(result);
  for (const [p, r] of PRONOUN_MAP) result = result.replace(p, r);
  for (const [p, r] of INTENSITY_MAP) result = result.replace(p, r);
  for (const [p, r] of ENDING_MAP) result = result.replace(p, r);
  result = polishConnectors(result);
  for (const [p, r] of REDUNDANCY_MAP) result = result.replace(p, r);
  result = result.replace(/\s{2,}/g, ' ').trim();
  result = normalizeSentenceEnds(result);

  return result;
}
